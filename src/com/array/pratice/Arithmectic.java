package com.array.pratice;

public class Arithmectic {
	public static void main(String args []) {
		float a=23.0F;
		float b=34.0F;
		
		System.out.println("a="+a);
		System.out.println("a="+b);
	
		System.out.println("a+b:"+(a+b));
		System.out.println("a+b:"+(a-b));
		System.out.println("a+b:"+(a*b));
		System.out.println("a+b:"+(a/b));
		System.out.println("a+b:"+(a%b));
		
	}


}
